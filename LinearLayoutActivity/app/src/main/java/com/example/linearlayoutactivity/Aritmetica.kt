package com.example.linearlayoutactivity

class Aritmetica {

    // Funcion para sumar 2 numeros
    fun suma(numeroUno: Float, numeroDos: Float):Float {
        return numeroUno + numeroDos
    }

    // Funcion para restar 2 numeros
    fun resta(numeroUno: Float, numeroDos: Float):Float {
        return numeroUno - numeroDos
    }

    // Funcion para multiplicar 2 numeros
    fun multiplicacion(numeroUno: Float, numeroDos: Float):Float {
        return numeroUno * numeroDos
    }

    // Funcion para dividir 2 numeros
    fun division(numeroUno: Float, numeroDos: Float):Float {
        return numeroUno / numeroDos
    }
}